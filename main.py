# Declarando a tupla
lanche = ('hamburguer', 'suco', 'pizza', 'pudim')

# Formas de manipular a tupla
print(lanche[1:]) # Printa o item com indice 1 até o último
print(lanche[-1]) # Printa o último item
len(lanche) # O tanto de elementos que há na tupla

# Estrutura de repetição com tupla
for c in lanche:
    print(c)